// Basic Arithmetic Operations in JavaScript

// Step 1: Create two number variables
const num1 = 10;
const num2 = 5;

// Step 2: Perform arithmetic operations
const sum = num1 + num2;          // Addition
const difference = num1 - num2;   // Subtraction
const product = num1 * num2;      // Multiplication
const quotient = num1 / num2;     // Division
const remainder = num1 % num2;    // Modulus (remainder)

// Step 3: Exponentiation
const num3 = num1 ** num2;        // num1 to the power of num2

// Step 4: Print all results to the console
console.log(`Basic Arithmetic Operations:`);
console.log(`num1 = ${num1}, num2 = ${num2}`);
console.log(`Sum: ${num1} + ${num2} = ${sum}`);
console.log(`Difference: ${num1} - ${num2} = ${difference}`);
console.log(`Product: ${num1} * ${num2} = ${product}`);
console.log(`Quotient: ${num1} / ${num2} = ${quotient}`);
console.log(`Remainder: ${num1} % ${num2} = ${remainder}`);
console.log(`Exponentiation: ${num1} ** ${num2} = ${num3}`);